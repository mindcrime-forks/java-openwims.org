    private void load(Annotation annotation) {
        this.DependenciesContainerJPanel.removeAll();
        
        String out = "";
        
        List<CoreMap> sentences = annotation.get(CoreAnnotations.SentencesAnnotation.class);
        for (CoreMap sentence : sentences) {
            List<CoreLabel> tokens = sentence.get(CoreAnnotations.TokensAnnotation.class);
            SemanticGraph graph = sentence.get(SemanticGraphCoreAnnotations.BasicDependenciesAnnotation.class);
            out += graph + "\n";
        }
        out = out.trim();
        
        JTextArea t = new JTextArea();
        t.setText(out);
        t.setEditable(false);
        
        this.DependenciesContainerJPanel.add(t);
        
        this.validate();
        this.repaint();
    }
    
    public void setAnnotation(Annotation annotation) {
        this.annotation = annotation;
        this.InputJTextArea.setText(this.annotation.toString());
        this.WIMifyJButton.setEnabled(true);
        
        load(this.annotation);
        
        this.validate();
        this.repaint();
    }
    
    private void fullProcess() {
        String input = this.InputJTextArea.getText().trim();
        if (input.equalsIgnoreCase("")) {
            return;
        }
        
        setAnnotation(StanfordHelper.annotate(input));
        wimify();
    }
    
    private void wimify() {
        if (this.annotation == null) {
            return;
        }
        
        LinkedList<WIMFrame> frames = WIMProcessor.WIMify(this.annotation);
        load(frames);
    }
    
    
    private class StanfordFilesDraggedEventListener implements FileDropJPanel.FilesDraggedListener {

        @Override
        public void filesDraggedEvent(List<File> files) {
            try {
                setAnnotation(StanfordHelper.load(files.get(0).getAbsolutePath()));
            } catch (Exception err) {
                err.printStackTrace();
            }
            
            wimify();
        }
        
    }
    
    private class SensesListSelectionListener implements ListSelectionListener {

        @Override
        public void valueChanged(ListSelectionEvent lse) {
            Sense sense = (Sense)SensesJList.getSelectedValue();
            if (sense == null) {
                return;
            }
            
            load(sense);
        }
        
    }
    
    private class SensesListCellRenderer extends DefaultListCellRenderer {

        @Override
        public Component getListCellRendererComponent(JList list, Object o, int i, boolean bln, boolean bln1) {
            JLabel label = (JLabel)super.getListCellRendererComponent(list, o, i, bln, bln1);
            
            if (o instanceof Sense) {
                Sense sense = (Sense) o;
                label.setText(sense.getId() + "  " + WIMGlobals.lexicon().definition(sense));
            }
            
            return label;
        }
        
    }
    
    private class SenseComparator implements Comparator<Sense> {

        @Override
        public int compare(Sense t, Sense t1) {
            return t.getId().compareTo(t1.getId());
        }
        
    }

